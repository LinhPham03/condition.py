so = int(input("Nhập 1 số bất kì, từ 1 đến 7: "))
if so == 1:
    print("Monday")
elif so == 2:
    print("Tuesday")
elif so == 3:
    print("Wednesday")
elif so == 4:
    print("Thursday")
elif so == 5:
    print("Friday")
elif so == 6:
    print("Saturday")
elif so == 7:
    print("Sunday")
else:
    print("error, out of range")